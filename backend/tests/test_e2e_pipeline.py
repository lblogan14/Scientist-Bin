# ---------------------------------------------------------------------------
# API-driven E2E test (via HTTP, verifies store integration)
# ---------------------------------------------------------------------------


@pytest.mark.slow
@pytest.mark.skipif(not _HAS_API_KEY, reason=_SKIP_REASON)
@pytest.mark.skipif(not IRIS_PATH.is_file(), reason=f"Dataset not found: {IRIS_PATH}")
@pytest.mark.timeout(600)
async def test_api_train_endpoint_e2e():
    """Submit a training request via the API and verify the experiment completes.

    Note: This test uses the real _run_training background task, so it requires
    a running event loop. We import the training function directly and await it.
    """
    import tempfile

    from scientist_bin_backend.api.experiments import (
        ExperimentStatus,
        ExperimentStore,
    )
    from scientist_bin_backend.api.routes import _run_training

    with tempfile.TemporaryDirectory() as tmpdir:
        store = ExperimentStore(store_dir=Path(tmpdir))
        experiment = store.create(
            objective="Classify iris via API E2E",
            data_file_path=str(IRIS_PATH),
            framework="sklearn",
        )
        exp_id = experiment.id

        # Patch the global store so _run_training uses our isolated one
        from unittest.mock import patch

        with patch("scientist_bin_backend.api.routes.experiment_store", store):
            await _run_training(
                experiment_id=exp_id,
                objective="Classify iris via API E2E",
                data_description="",
                data_file_path=str(IRIS_PATH),
                framework="sklearn",
                auto_approve_plan=True,
                deep_research=False,
                budget_max_iterations=10,
                budget_time_limit_seconds=600.0,
            )

        updated = store.get(exp_id)
        assert updated is not None
        assert updated.status == ExperimentStatus.completed, (
            f"Expected completed, got {updated.status}. Result: {updated.result}"
        )
        assert updated.result is not None
        assert updated.problem_type == "classification"
        assert updated.framework == "sklearn"
