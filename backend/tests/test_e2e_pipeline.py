"""End-to-end pipeline tests that run the actual ML agent with real datasets.

These tests invoke the real LLM (Google Gemini) and execute real training code.
They are marked ``@pytest.mark.slow`` and skipped when ``GOOGLE_API_KEY`` is
not set, so they run only in local development or dedicated CI stages.

Run with::

    uv run pytest tests/test_e2e_pipeline.py -v --timeout=600 -m slow

"""

